				el envío de la información por medios físicos y electrónicos.
			</p>
			<p class="text-justify text-int">
				b) Facilitar al cliente información que le permita acceder a ofertas y promociones.
			</p>
			<p class="text-justify text-int">
				c) Informar sobre nuevos productos y/o servicios
				.</p>
				<p class="text-justify text-int">
					d) Dar cumplimiento a obligaciones contraídas con nuestros clientes, proveedores y empleados.
				</p>
				<p class="text-justify text-int">
					e) Informar sobre cambios de nuestros productos y/o servicios.
				</p>
				<p class="text-justify text-int">
					f) Evaluar la calidad de nuestros productos y/o servicios.
				</p>
				<p class="text-justify text-int">
					g) Socialización de políticas, programas, resultados y cambios organizacionales.
				</p>
				<p class="text-justify text-int">
					h) Análisis de la información para desarrollo e implementación de estrategias comerciales en cumplimiento del
