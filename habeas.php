					i) Establecer comunicación entre ORTOPÉDICOS FUTURO y los Titulares para cualquier propósito relacionado con
					las finalidades que se establecen en la presente política, ya sea mediante llamadas, mensajes de texto, correos
					electrónicos y/o físicos.
				</p>
				<p class="text-justify text-int">
					j) Efectuar o implementar la adquisición u oferta de productos o servicios por parte de ORTOPÉDICOS FUTURO.
