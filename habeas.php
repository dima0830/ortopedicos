					b) Solicitar prueba de la autorización otorgada a ORTOPÉDICOS FUTURO, salvo en los casos expresamente
					exceptuados en la Ley.
				</p>
				<p class="text-justify text-int">
					c) Ser informado por ORTOPÉDICOS FUTURO, previa solicitud, respecto del uso que le ha dado a sus datos personales.
