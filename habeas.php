				2. MARCO LEGAL
			</p>
			<p class="text-justify text-int">
				La presente política de protección y tratamiento de datos personales se ajusta a lo establecido en la Ley 1581 de 2012, el Decreto
				1377 de 2013, la Circular 02 de la Superintendencia de Industria y Comercio de noviembre de 2015 y las normas que lo modifiquen o complementen.
			</p>
			<p class="text-justify text-int">
				El objetivo de la Ley es desarrollar el derecho constitucional que tienen todas las personas a conocer, actualizar y rectificar las
				informaciones que se hayan recogido sobre ellas en bases de datos o archivos, y los demás derechos, libertades y garantías constitucionales
				a que se refiere el artículo 15 de la Constitución Política (derecho a la intimidad personal, familiar y al buen nombre),
				así como el derecho a la información consagrado en el artículo 20 de la misma.
			</p>
			<p class="text-justify text-int">
				Se exceptúan de la aplicación de la Ley 1581 de 2012 y del Decreto 1377 de 2013, lo relativo a las bases de datos mantenidas en
				un ámbito exclusivamente personal o doméstico, es decir, las actividades comprendidas en el marco de la vida privada o familiar
				de las personas naturales y las otras especificadas en el artículo 2 de la citada Ley.
