					<br>
					11. PROCEDIMIENTO DE RECLAMOS, REVOCACIÓN DE AUTORIZACIÓN, RETIRO O SUPRESIÓN DE LAS BASES DE
					DATOS.
				</p>
				<p class="text-justify text-int">
					La solicitud la podrá presentar el Titular, teniendo en cuenta la información señalada en el artículo 15 de
