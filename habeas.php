			<p class="text-justify text-int">
				3. DEFINICIONES ORTOPÉDICOS FUTURO tiene en cuenta las siguientes definiciones en el tratamiento de datos personales:
			</p>
			<p class="text-justify text-int">
				a) Autorización: Consentimiento previo, expreso e informado del Titular para llevar a cabo el Tratamiento de datos
				personales.
			</p>
			<p class="text-justify text-int">
				b) Base de Datos: Conjunto organizado de datos personales que sea objeto de Tratamiento.
			</p>
			<p class="text-justify text-int">
				c) Dato personal: Cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas
				o determinables.
			</p>
			<p class="text-justify text-int">
				d) Encargado del Tratamiento: Persona natural o jurídica, pública o privada, que por sí misma o en asocio con
				otros, realice el Tratamiento de datos personales por cuenta del Responsable del Tratamiento.
			</p>
			<p class="text-justify text-int">
				e) Responsable del Tratamiento: Persona natural o jurídica, pública o privada, que por sí misma o en asocio con
				otros, decida sobre la base de datos y el Tratamiento de los datos.
			</p>
			<p class="text-justify text-int">
				f) Titular: Persona natural cuyos datos personales sean objeto de Tratamiento.
			</p>
			<p class="text-justify text-int">
				g) Tratamiento: Cualquier operación o conjunto de operaciones sobre datos personales, tales como la recolección,
				almacenamiento, uso, circulación o supresión.
			</p>
			<p class="text-justify text-int">
				<br>
				4. DATOS RECOLECTADOS Y FORMA DE RECOLECCIÓN
			</p>
			<p class="text-justify text-int">
				ORTOPÉDICOS FUTURO podrá pedir la información expresamente a los Titulares o recolectar los datos que sean
				necesarios para cumplir la finalidad de las bases de datos, los cuales son -entre otros- su nombre y apellidos,
				número de identificación, fecha de nacimiento, dirección de correspondencia, teléfono de contacto, correo electrónico,
				experiencia, antecedentes comerciales, judiciales, relaciones comerciales con otras compañías o con entidades
				públicas, lugar de trabajo, referencias e historia laboral, necesidades e intereses. Los datos podrán ser suministrados
				explícitamente a ORTOPÉDICOS FUTURO mediante formatos de ingreso o vinculación, formatos de
				autorización, recolectados personalmente a través de sus empleados y prestadores de servicios o de los comportamientos
				y conductas inequívocas de los Titulares como reclamaciones, solicitudes de cotización, propuestas,
				ofertas, solicitudes de empleo, de participación en proyectos, suscripción de contratos con ORTOPÉDICOS
				FUTURO, entre otras. En cualquier caso, los Titulares se comprometen a que la información entregada con relación
				a sus datos personales es veraz y verificable por lo que ORTOPÉDICOS FUTURO se reserva la posibilidad de eliminar
				la información que no cumpla con estas condiciones de sus bases de datos y en todo caso sin requerir autorización
				del Titular respectivo.
			</p>
			<p class="text-justify text-int">
				<br>
				5. AUTORIZACIÓN PARA LA RECOLECCIÓN Y TRATAMIENTO DE DATOS PERSONALES Y OTRA INFORMACIÓN.
				Mediante el suministro voluntario de alguno de los datos personales en la forma señalada en el numeral anterior,
				y/o la autorización expresa, el Titular autoriza expresa e inequívocamente a ORTOPÉDICOS FUTURO para recolectar
				sus datos personales y cualquier otra información que suministre, así como para realizar el tratamiento sobre
				sus datos personales, de conformidad con esta Política y la ley. La autorización en mención podrá darse por escrito,
				de forma oral o mediante conductas inequívocas del Titular, que permitan concluir de forma razonable que
				otorgó la autorización pues de no haberse surtido la conducta, los datos no se habrían capturado y almacenado
				en las bases de datos, tales como la suscripción y creación de un perfil en la página web: www.ortopedicosfuturo.com.
			</p>
