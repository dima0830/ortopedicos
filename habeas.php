					En cualquier momento y de manera gratuita el Titular o sus causahabientes podrán solicitar a través del correo
					electrónico antes mencionado, la rectificación, actualización o supresión de sus datos personales, previa
					acreditación de su identidad. La solicitud debe contener, como mínimo, nombre y domicilio del titular o cualquier
					otro medio para recibir la respuesta, documentos que acrediten la identidad o la personalidad de su
					representante, documentos que quiera hacer valer, descripción de los hechos que dan lugar al reclamo y la
					descripción clara y precisa de los datos personales respecto de los cuales el titular busca ejercer su derecho de
					reclamo.
				</p>
				<p class="text-justify text-int">
					Si el reclamo resulta incompleto, se requerirá al interesado dentro de los cinco (5) días siguientes a la recepción
					del reclamo para que subsane las fallas. Transcurridos dos (2) meses desde la fecha del requerimiento, sin que
					el solicitante presente la información requerida, se entenderá que ha desistido del reclamo.
					En caso de que quien reciba el reclamo no sea competente para resolverlo, dará traslado a quien corresponda
					en un término máximo de dos (2) días hábiles e informará de la situación al interesado.
					El término máximo para atender el reclamo será de quince (15) días hábiles contados a partir del día siguiente
					a la fecha de su recibo. Cuando no fuere posible atender el reclamo dentro de dicho término, se informará al
					interesado los motivos de la demora y la fecha en que se atenderá su reclamo, la cual en ningún caso podrá
					superar los ocho (8) días hábiles siguientes al vencimiento del primer término.
