			</p>
			<p class="text-justify text-int">
				ORTOPÉDICOS FUTURO, trata y maneja los datos personales de los Titulares, dentro del marco del giro natural de
				su gestión y objeto social como proveedor de medias de compresión y otros tipos de dispositivos médicos dirigidos
				al sector de la salud en Colombia, específicamente para las siguientes finalidades:
			</p>
			<p class="text-justify text-int">
